/*
 * Generate a random anonymous username based on a select list of
 * gerunds (-ing form verbs), and nouns.
 */
export const generateName = () => {
    // select a random element from the list
    let thisGerund = gerund[Math.floor(Math.random() * gerund.length)];
    let thisNoun = noun[Math.floor(Math.random() * noun.length)];

    // perform formatting
    thisGerund = thisGerund.charAt(0).toUpperCase() + thisGerund.slice(1);
    thisNoun = thisNoun.charAt(0).toUpperCase() + thisNoun.slice(1);

    return `${thisGerund} ${thisNoun}`;
};
